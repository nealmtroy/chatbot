CREATE INDEX IF NOT EXISTS idx_vip_payments_next_check ON public.vip_payments (next_check_at ASC, id ASC) WHERE status IN ('pending', 'invite_error', 'delivery_error');
CREATE INDEX IF NOT EXISTS idx_vip_payments_package_code ON public.vip_payments (package_code);

CREATE UNIQUE INDEX IF NOT EXISTS idx_vip_payments_one_active_per_user ON public.vip_payments (user_id) WHERE status IN ('pending', 'processing_paid', 'invite_error', 'processing_delivery', 'delivery_error');

