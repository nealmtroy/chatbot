CREATE INDEX IF NOT EXISTS idx_vip_users_invited_by ON public.vip_users (invited_by_user_id);
CREATE INDEX IF NOT EXISTS idx_vip_users_broadcast_order ON public.vip_users (is_bot, last_broadcast_at ASC NULLS FIRST, user_id);

CREATE INDEX IF NOT EXISTS idx_vip_broadcast_messages_active ON public.vip_broadcast_messages (is_active, id DESC);
